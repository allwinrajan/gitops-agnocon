#!/bin/sh
# =============================================================================
# startup.sh вҖ” Admin UI (nginx + Vite static build)
# =============================================================================
# Vite bakes env vars into the JS bundle at BUILD time using import.meta.env.
# Since .env values change per environment, we use runtime injection:
#
# 1. Fix routes + DNS (Multus MacVLAN side effects)
# 2. Fetch all values from Consul KV under admin-frontend/
# 3. Write /usr/share/nginx/html/env-config.js  вҶ’  window._env_ = { ... }
# 4. sed-replace build-time placeholder values in compiled JS bundle
# 5. exec nginx -g 'daemon off;'
# =============================================================================

PREFIX="admin-frontend"

# =============================================================================
# BLOCK 0 вҖ” FIX ROUTES + DNS
# =============================================================================
echo "[0/4] Pre-flight: fixing routes and DNS..."
ip route add 10.96.0.0/12  via 169.254.1.1 dev eth0 2>/dev/null || true
ip route add 10.244.0.0/16 via 169.254.1.1 dev eth0 2>/dev/null || true
echo "      вң“ Routes fixed"

printf 'nameserver 10.96.0.10\nsearch admin-app.svc.cluster.local svc.cluster.local cluster.local\noptions ndots:5\n' > /etc/resolv.conf
echo "      вң“ DNS restored (CoreDNS: 10.96.0.10)"

CONSUL_URL="${CONSUL_URL}"
CONSUL_TOKEN="${CONSUL_TOKEN}"
# CONSUL_URL / CONSUL_TOKEN are injected by the pod spec from
# .Values.consul.url / .Values.consul.token (see deployment.yaml).
# No hardcoded fallback вҖ” fail fast if the pod spec did not inject them.
if [ -z "${CONSUL_URL}" ] || [ -z "${CONSUL_TOKEN}" ]; then
    echo "  вңҳ  CONSUL_URL/CONSUL_TOKEN not set вҖ” check pod env injection" >&2
    exit 1
fi

echo "================================================================="
echo " Admin UI вҖ” Startup (nginx)"
echo " Pod: ${POD_NAME:-unknown}"
echo " Consul: ${CONSUL_URL}"
echo "================================================================="

# =============================================================================
# BLOCK 1 вҖ” FETCH FROM CONSUL (wget вҖ” nginx:alpine has wget, not curl)
# =============================================================================
consul_get() {
    _key="${PREFIX}/${1}"
    _varname="${2}"
    _val=$(wget -qO- \
        --no-check-certificate \
        --timeout=10 \
        --header="X-Consul-Token: ${CONSUL_TOKEN}" \
        "${CONSUL_URL}/v1/kv/${_key}?raw" 2>/dev/null) || true
    if [ -z "${_val}" ]; then
        echo "  вң— MISSING: ${_key}" >&2
        exit 1
    fi
    export "${_varname}=${_val}"
    echo "  вң“ ${_key}"
}

echo ""
echo "[1/4] Fetching config from Consul (${PREFIX}/)"
echo ""

consul_get "api_base_url"            VITE_API_BASE_URL
consul_get "google_oauth_client_id"  VITE_GOOGLE_OAUTH_CLIENT_ID
consul_get "crypto_secret_key"       VITE_CRYPTO_SECRET_KEY
consul_get "superset_url"            VITE_SUPERSET_URL
consul_get "superset_sdk_domain"     VITE_SUPERSET_SDK_DOMAIN
consul_get "superset_username"       VITE_SUPERSET_USERNAME
consul_get "superset_password"       VITE_SUPERSET_PASSWORD
consul_get "webhook_url"             VITE_WEBHOOK_URL

echo ""
echo "  All keys resolved вң“"

# =============================================================================
# BLOCK 2 вҖ” INJECT ENV VARS INTO NGINX HTML
# =============================================================================
echo ""
echo "[2/4] Injecting env vars into nginx HTML..."

NGINX_HTML="/usr/share/nginx/html"

# в”Җв”Җ Approach A: window._env_ config file в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
# This file is loaded at runtime by index.html BEFORE the Vite bundle.
# All 8 KV values are written here so window._env_ is always complete.
cat > "${NGINX_HTML}/env-config.js" << ENVEOF
// Auto-generated by startup.sh вҖ” DO NOT EDIT
// Source: Consul KV prefix: ${PREFIX}/
// Pod: ${POD_NAME:-unknown}
window._env_ = {
  VITE_API_BASE_URL: "${VITE_API_BASE_URL}",
  VITE_GOOGLE_OAUTH_CLIENT_ID: "${VITE_GOOGLE_OAUTH_CLIENT_ID}",
  VITE_CRYPTO_SECRET_KEY: "${VITE_CRYPTO_SECRET_KEY}",
  VITE_SUPERSET_URL: "${VITE_SUPERSET_URL}",
  VITE_SUPERSET_SDK_DOMAIN: "${VITE_SUPERSET_SDK_DOMAIN}",
  VITE_SUPERSET_USERNAME: "${VITE_SUPERSET_USERNAME}",
  VITE_SUPERSET_PASSWORD: "${VITE_SUPERSET_PASSWORD}",
  VITE_WEBHOOK_URL: "${VITE_WEBHOOK_URL}"
};
ENVEOF
echo "  вң“ env-config.js written (all 8 keys)"

# в”Җв”Җ Inject <script src="/env-config.js"> into index.html if not present в”Җв”Җв”Җв”Җв”Җв”Җв”Җ
# Must come BEFORE the Vite bundle script so window._env_ is ready when
# the app JS executes.
if ! grep -q "env-config.js" "${NGINX_HTML}/index.html" 2>/dev/null; then
    sed -i 's|<head>|<head><script src="/env-config.js"></script>|' "${NGINX_HTML}/index.html"
    echo "  вң“ env-config.js injected into index.html <head>"
else
    echo "  вң“ env-config.js already referenced in index.html"
fi

# в”Җв”Җ Approach B: Replace ALL baked-in placeholder values in compiled JS в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
# Each sed rule targets the exact placeholder string that was baked into the
# Vite bundle at image build time (from your .env.* file).
#
# HOW TO FIND YOUR PLACEHOLDERS:
#   grep -r "localhost\|192\.168\|YOUR_PLACEHOLDER" /usr/share/nginx/html/assets/
#
# Replace the right-hand side strings below with whatever your build .env
# actually contained at docker build time.
# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
echo "  Patching compiled JS assets (all 8 KV values)..."

find "${NGINX_HTML}/assets" -name "*.js" 2>/dev/null | while read -r jsfile; do

    # в”Җв”Җ VITE_API_BASE_URL в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    # Catches any 192.168.x.x:4001 (http or https) baked at build time,
    # plus localhost fallbacks.
    sed -i \
        -e "s|https\?://192\.168\.[0-9]*\.[0-9]*:4001|${VITE_API_BASE_URL}|g" \
        -e "s|http://localhost:4001|${VITE_API_BASE_URL}|g" \
        -e "s|https://localhost:4001|${VITE_API_BASE_URL}|g" \
        "${jsfile}"

    # в”Җв”Җ VITE_GOOGLE_OAUTH_CLIENT_ID в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    # Replace the build-time placeholder Google OAuth Client ID.
    # Update the pattern below to match your actual build-time value.
    sed -i \
        -e "s|YOUR_BUILD_TIME_GOOGLE_CLIENT_ID\.apps\.googleusercontent\.com|${VITE_GOOGLE_OAUTH_CLIENT_ID}|g" \
        "${jsfile}"

    # в”Җв”Җ VITE_CRYPTO_SECRET_KEY в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    sed -i \
        -e "s|NmglZwN3l6PTpGIrxkSihiXQ4YEFE7xCdXQFzR3l3i40l1ffCnmR5|${VITE_CRYPTO_SECRET_KEY}|g" \
        "${jsfile}"

    # в”Җв”Җ VITE_SUPERSET_URL в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    sed -i \
        -e "s|https\?://192\.168\.[0-9]*\.[0-9]*:8088|${VITE_SUPERSET_URL}|g" \
        -e "s|http://localhost:8088|${VITE_SUPERSET_URL}|g" \
        -e "s|https://localhost:8088|${VITE_SUPERSET_URL}|g" \
        "${jsfile}"

    # в”Җв”Җ VITE_SUPERSET_SDK_DOMAIN в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    # Replace the build-time placeholder Superset SDK domain.
    # Update the pattern below to match your actual build-time value.
    sed -i \
        -e "s|YOUR_BUILD_TIME_SUPERSET_SDK_DOMAIN|${VITE_SUPERSET_SDK_DOMAIN}|g" \
        "${jsfile}"

    # в”Җв”Җ VITE_SUPERSET_USERNAME в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    # Update the pattern below to match your actual build-time value.
    sed -i \
        -e "s|YOUR_BUILD_TIME_SUPERSET_USERNAME|${VITE_SUPERSET_USERNAME}|g" \
        "${jsfile}"

    # в”Җв”Җ VITE_SUPERSET_PASSWORD в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    # Update the pattern below to match your actual build-time value.
    sed -i \
        -e "s|YOUR_BUILD_TIME_SUPERSET_PASSWORD|${VITE_SUPERSET_PASSWORD}|g" \
        "${jsfile}"

    # в”Җв”Җ VITE_WEBHOOK_URL в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    # Catches any 192.168.x.x or localhost webhook URL baked at build time.
    # Update the pattern below to match your actual build-time value.
    sed -i \
        -e "s|https\?://192\.168\.[0-9]*\.[0-9]*:[0-9]*/webhook|${VITE_WEBHOOK_URL}|g" \
        -e "s|http://localhost:[0-9]*/webhook|${VITE_WEBHOOK_URL}|g" \
        -e "s|YOUR_BUILD_TIME_WEBHOOK_URL|${VITE_WEBHOOK_URL}|g" \
        "${jsfile}"

done
echo "  вң“ Compiled JS assets patched (all 8 KV values)"

# в”Җв”Җ Verification в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
echo ""
echo "  Verifying patches..."

check_residual() {
    _pattern="${1}"
    _label="${2}"
    FOUND=$(grep -roh "${_pattern}" "${NGINX_HTML}/assets/" 2>/dev/null | sort -u)
    if [ -n "${FOUND}" ]; then
        echo "  вҡ  WARNING: old ${_label} still found in bundle: ${FOUND}" >&2
    else
        echo "  вң“ No old ${_label} in bundle"
    fi
}

check_residual "https\?://192\.168\.[0-9]*\.[0-9]*:4001"  "API URLs (:4001)"
check_residual "https\?://192\.168\.[0-9]*\.[0-9]*:8088"  "Superset URLs (:8088)"
check_residual "NmglZwN3l6PTpGIrxkSihiXQ4YEFE7xCdXQFzR3l3i40l1ffCnmR5" "crypto_secret_key placeholder"

echo ""
echo "  в”Җв”Җ Runtime values injected в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ"
echo "    API Base URL         : ${VITE_API_BASE_URL}"
echo "    Google OAuth Client  : ${VITE_GOOGLE_OAUTH_CLIENT_ID}"
echo "    Crypto Secret Key    : (set, not shown)"
echo "    Superset URL         : ${VITE_SUPERSET_URL}"
echo "    Superset SDK Domain  : ${VITE_SUPERSET_SDK_DOMAIN}"
echo "    Superset Username    : ${VITE_SUPERSET_USERNAME}"
echo "    Superset Password    : (set, not shown)"
echo "    Webhook URL          : ${VITE_WEBHOOK_URL}"
echo "  в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ"

# =============================================================================
# BLOCK 3 вҖ” LAUNCH NGINX
# =============================================================================
echo ""
echo "[3/4] Launching nginx..."
echo "================================================================="

exec nginx -g "daemon off;"
